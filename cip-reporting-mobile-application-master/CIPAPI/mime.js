  CIPAPI.mime.set = function(exts, mime_type_string) {
    $.each(exts.split(','), function(index, ext) {
      catalog[ext] = mime_type_string;
    });
  }

  CIPAPI.mime.getMimeTypeForFileName = function(fileName) {
    var dotOffset = fileName.lastIndexOf('.');
    var ext = dotOffset > 0 ? fileName.substr(dotOffset).toLowerCase() : fileName.toLowerCase();
    log.debug("Extension: " + ext);
    
    var mime = (typeof catalog[ext] == 'undefined') ? 'application/octet-stream' : catalog[ext];
    log.debug("Mime: " + mime);
    
    return mime;
  }
  
  CIPAPI.mime.getExtensionForMimeType = function(mimeType) {
    var found = false;
    
    $.each(catalog, function(ext, mime) {
      if (mimeType == mime) {
        log.debug(mimeType + ' extension is ' + ext);
        found = ext;
        return false;
      }
    });
    
    return found;
  }
  
